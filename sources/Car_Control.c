void T_change()
{
	int i=0;
	for(i=0;i<ROW;i++)
	{
		if(FlagCenter[i] > 159)
			Center_T[i] = X_table[i][159];
		else if(FlagCenter[i] < 0)
			Center_T[i] = X_table[i][0];
		else
			Center_T[i] = X_table[i][FlagCenter[i]];
		
		if(FlagR[i] > 159)
			Center_R[i] = X_table[i][159];
		else if(FlagR[i] < 0)
			Center_R[i] = X_table[i][0];
		else
			Center_R[i] = X_table[i][FlagR[i]];
		
		if(FlagL[i] > 159)
			Center_L[i] = X_table[i][159];
		else if(FlagL[i] < 0)
			Center_L[i] = X_table[i][0];
		else
			Center_L[i] = X_table[i][FlagL[i]];
	
	}
}
/*********************************************************** 
函数名称：Speed_set
函数功能：设定出差速
入口参数：Voutside  轮外边速度  单位 mm/s  
		theta 舵机打角值
出口参数：无  
***********************************************************/
static void Speed_set(int16 Voutside,int16 theta)
{
	int16 VRight,VLeft;
	if(theta>Mid_Steer+25)//差速设定//左转
	{
		if(theta>Mid_Steer+150)
		{
			theta = Mid_Steer + 150 ;
		}
		VRight = 0.86 * Voutside * (1.0 + 3 * (theta - Mid_Steer) / 2500);
		VLeft = 0.86 * Voutside * (1.0 - 3 * (theta - Mid_Steer) / 2500);
	}
	else if((theta<Mid_Steer-25))
	{
		if(theta < Mid_Steer - 150)
		{
			theta = Mid_Steer - 150; 
		}
 		VRight = 0.86 * Voutside * (1.0 + 3 * (theta - Mid_Steer) / 2500);
		VLeft = 0.86 * Voutside * (1.0 - 3 * (theta - Mid_Steer) / 2500);
	}
	else 
	{
		VLeft=VRight=Voutside;	
	}
	SetPoint_L  = VLeft/50;//将mm/s换算成编码器脉冲数
	SetPoint_R  = VRight/50;//将mm/s换算成编码器脉冲数
}

